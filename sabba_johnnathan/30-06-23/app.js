// arrendondar numeros 
const number = 3.14159;

console.log(Math.round(number)); // Output: 3
console.log(Math.floor(number)); // Output: 3
console.log(Math.ceil(number));  // Output: 4


// potencia
const base = 2;
const exponent = 3;

console.log(Math.pow(base, exponent));   // Output: 8
console.log(base ** exponent);           // Output: 8 
console.log(Math.sqrt(base));  

// minimo e maximo
console.log(Math.min(1, 2, 3, 4, 5));  // Output: 1
console.log(Math.max(1, 2, 3, 4, 5));  // Output: 5